// Variable declaration
var tomatoPrice = 25;

// Variable value update
tomatoPrice = 40;

// Add something to the already assigned variable
var price1 = 90;
price1 = price1 + 15;
console.log(price1)

// Shorthand
price1 += 5;
console.log(price1)
price1 -= 30;
console.log(price1)
price1 *= 2;
console.log(price1)
price1 /= 3;
console.log(price1)

// ++ refers to adding 1 to the previous value; -- refers to deducting 1 from the previous value

var age = 18;
age++;
console.log(age)

var love = 100;
love--;
console.log(love)
